				<br>
				Проанализировав рынок, а также пожелания и отзывы наших заказчиков, было принято решение о проектировании автожира совмещающего в себе свободу открытого полёта и комфорт закрытой кабины. Так на свет появился первый на рынке универсальный открыто-закрытый автожир <a target="_blank" href="http://autogyro-izhevsk.com/assets/video/travel.mp4"><strong>ТАНГО ТРЕВЕЛ</strong></a>. Этот автожир круглогодичного использования имеет систему отопления салона. Модульная конструкция фонаря позволяет трансформировать кабину из открытой в закрытую и наоборот в течение 5-ти минут. Полетать с ветерком или спрятаться от непогоды внутри уютной кабины - выбор за Вами!<br>
				<br>
				Так же , в течение 8 лет нашим коллективом изготавливаются роторы для автожиров.<br>
				<br>
				На свои аппараты устанавливаем двигатели фирмы Ямаха. <a href="https://www.youtube.com/watch?v=mVzLZSeZ9g8&feature=youtu.be" target="_blank"><strong>Мощные</strong></a>, современные, надёжные двигатели значительно улучшили летные характеристики наших автожиров.<br>
				<br>
				Владельцы автожиров, изготовленных с нашей помощью, принимают участие в выставках, успешно выступают на чемпионатах России, Европы, <a href="https://youtu.be/tWeh5PrKbAY" target="_blank"><strong>мира</strong></a>. Автожиры Танго применяются при патрулировании и производстве cельскохозяйственных работ.<br>
				<br>
				Сегодня большое количество энтузиастов пытаются самостоятельно построить автожиры и сталкиваются с большими трудностями по изготовлению отдельных узлов и элементов Автожира.<br>
